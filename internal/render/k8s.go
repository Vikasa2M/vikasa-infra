// prometheusRuleTmpl ships a starter set of NATS/JetStream alerts (operators tune
// the exprs to their exporter version). The regional leaf alert is omitted on the
// central cluster. The JetStream* alerts use the exporter's JSZ collector
// (gnatsd_jsz_* metrics, which require the exporter's -jsz flag); like the varz
// exprs they are starters — verify the metric names against your exporter version.
var prometheusRuleTmpl = template.Must(template.New("prometheusrule").Parse(`# AUTOGENERATED by vikasa-infra/cmd/gen
apiVersion: monitoring.coreos.com/v1
kind: PrometheusRule
metadata:
  name: vikasa-nats-{{ .Cluster }}
  namespace: {{ .Namespace }}
  labels:
    vikasa.dot: {{ .DOT }}
    vikasa.cluster: {{ .Cluster }}
    release: {{ .Release }}
spec:
  groups:
    - name: vikasa-nats-{{ .Cluster }}
      rules:
        - alert: NatsServerDown
          expr: up{job="nats", namespace="{{ .Namespace }}"} == 0
          for: 5m
          labels:
            severity: critical
            vikasa_cluster: {{ .Cluster }}
          annotations:
            summary: "NATS server down in cluster {{ .Cluster }}"
        - alert: NatsSlowConsumers
          expr: gnatsd_varz_slow_consumers{namespace="{{ .Namespace }}"} > 0
          for: 10m
          labels:
            severity: warning
            vikasa_cluster: {{ .Cluster }}
          annotations:
            summary: "NATS slow consumers in cluster {{ .Cluster }}"
        - alert: JetStreamStorageHigh
          expr: gnatsd_jsz_max_storage{namespace="{{ .Namespace }}"} > 0 and gnatsd_jsz_storage{namespace="{{ .Namespace }}"} / gnatsd_jsz_max_storage{namespace="{{ .Namespace }}"} > 0.8
          for: 10m
          labels:
            severity: critical
            vikasa_cluster: {{ .Cluster }}
          annotations:
            summary: "JetStream store >80% of max in cluster {{ .Cluster }} — publishers fail when full (finding C2)"
        - alert: JetStreamConsumerLagging
          expr: gnatsd_jsz_consumer_num_pending{namespace="{{ .Namespace }}"} > 100000
          for: 15m
          labels:
            severity: warning
            vikasa_cluster: {{ .Cluster }}
          annotations:
            summary: "JetStream consumer backlog >100k in cluster {{ .Cluster }} — a sink is falling behind"
        - alert: JetStreamAssetsHigh
          expr: gnatsd_jsz_total_streams{namespace="{{ .Namespace }}"} + gnatsd_jsz_total_consumers{namespace="{{ .Namespace }}"} > 1500
          for: 30m
          labels:
            severity: warning
            vikasa_cluster: {{ .Cluster }}
          annotations:
            summary: "JetStream HA-assets approaching the ~2k/node budget in cluster {{ .Cluster }} — add nodes or partitions"
{{- if not .IsCentral }}
        - alert: NatsLeafDown
          expr: gnatsd_varz_leafnodes{namespace="{{ .Namespace }}"} < 1
          for: 5m
          labels:
            severity: warning
            vikasa_cluster: {{ .Cluster }}
          annotations:
            summary: "Regional cluster {{ .Cluster }} has no leaf uplink to central"
{{- end }}
`))

type prometheusRuleData struct {
	DOT       string
	Cluster   string
	Namespace string
	Release   string
	IsCentral bool
}

// kustomizationTmpl is the per-k8s-cluster Kustomize overlay listing the
// cluster's deployable manifests. Identical across clusters except the central
// cluster, which additionally lists the credhealth PrometheusRule. Tool-agnostic:
// works with `kubectl apply -k` and Argo CD.
var kustomizationTmpl = template.Must(template.New("kustomization").Parse(`# AUTOGENERATED by vikasa-infra/cmd/gen
apiVersion: kustomize.config.k8s.io/v1beta1
kind: Kustomization
resources:
  - streams.yaml
  - certificate.yaml
  - externalsecret.yaml
  - servicemonitor.yaml
  - prometheusrule.yaml
{{- if .IsCentral }}
  - credhealth-prometheusrule.yaml
{{- end }}
`))

// credhealthRuleData is prometheusRuleData plus the credhealth exporter's
// metric names, injected from internal/credhealth's exported constants so
// the alert exprs below can never drift from what the exporter actually
// emits (see internal/render's TestCredhealthMetricNamesNoDrift).
type credhealthRuleData struct {
	prometheusRuleData
	MetricCredExpirySeconds  string
	MetricCredHealthLastScan string
}

// credhealthRuleTmpl ships the slip→cliff credential-expiry alerts. Emitted only
// on the central cluster (the issuer is the source of truth for cred expiry); the
// metric source is cmd/credhealth -metrics-out via the node_exporter textfile
// collector. The {{"{{ ... }}"}} actions emit literal Prometheus label templating
// (Go's text/template would otherwise parse the inner braces as its own action).
// exprs are starter thresholds (40d / 5d / expired / 24h-stale) operators tune.
var credhealthRuleTmpl = template.Must(template.New("credhealthrule").Parse(`# AUTOGENERATED by vikasa-infra/cmd/gen
apiVersion: monitoring.coreos.com/v1
kind: PrometheusRule
metadata:
  name: vikasa-credhealth-{{ .Cluster }}
  namespace: {{ .Namespace }}
  labels:
    vikasa.dot: {{ .DOT }}
    vikasa.cluster: {{ .Cluster }}
    release: {{ .Release }}
spec:
  groups:
    - name: vikasa-credhealth-{{ .Cluster }}
      rules:
        - alert: CredExpiringSoon
          expr: {{ .MetricCredExpirySeconds }} < 3456000
          for: 1h
          labels:
            severity: warning
          annotations:
            summary: "Credential {{"{{ $labels.identity }}"}} ({{"{{ $labels.kind }}"}}) expires in under 40 days"
        - alert: CredExpiryCritical
          expr: {{ .MetricCredExpirySeconds }} < 432000
          for: 30m
          labels:
            severity: critical
          annotations:
            summary: "Credential {{"{{ $labels.identity }}"}} ({{"{{ $labels.kind }}"}}) expires in under 5 days"
        - alert: CredExpired
          expr: {{ .MetricCredExpirySeconds }} < 0
          for: 5m
          labels:
            severity: critical
          annotations:
            summary: "Credential {{"{{ $labels.identity }}"}} ({{"{{ $labels.kind }}"}}) has EXPIRED"
        - alert: CredHealthStale
          expr: time() - {{ .MetricCredHealthLastScan }} > 86400
          for: 15m
          labels:
            severity: critical
          annotations:
            summary: "credhealth has not reported in over 24h (expiry monitor is blind)"
`))

// leafHost strips a trailing :port from a leaf-endpoint (the host has no other colons).
func leafHost(endpoint string) string {
	if i := strings.LastIndex(endpoint, ":"); i >= 0 {
		return endpoint[:i]
	}
	return endpoint
}

// K8sRenderer renders one cluster's streams as NACK Stream CRs.
type K8sRenderer struct{}

// RenderCluster implements SubstrateRenderer. It emits a single streams.yaml
// containing one NACK Stream CR per stream on this cluster (multi-doc, in the
// order Dispatch already sorted them by name).
func (K8sRenderer) RenderCluster(c ClusterSlice) (map[string][]byte, error) {
	var buf bytes.Buffer
	buf.WriteString(banner)
	for idx, s := range c.Streams {
		if idx > 0 {
			buf.WriteString("---\n")
		}
		doc, err := renderStreamCR(s, c.DOT)
		if err != nil {
			return nil, err
		}
		buf.Write(doc)
	}
	host := leafHost(c.LeafEndpoint)
	dnsNames := []string{host}
	if c.Namespace != "" {
		dnsNames = append(dnsNames, "nats."+c.Namespace+".svc.cluster.local")
	}
	var certBuf bytes.Buffer
	if err := certTmpl.Execute(&certBuf, certData{
		Name:       c.ID + "-nats-server",
		DOT:        c.DOT,
		Cluster:    c.ID,
		SecretName: c.ID + "-nats-server-tls",
		Issuer:     c.IssuerName,
		CommonName: host,
		DNSNames:   dnsNames,
	}); err != nil {
		return nil, fmt.Errorf("k8s render: certificate %s: %w", c.ID, err)
	}

	var esBuf bytes.Buffer
	if err := externalSecretTmpl.Execute(&esBuf, externalSecretData{
		DOT:         c.DOT,
		Cluster:     c.ID,
		SecretStore: c.SecretStore,
		RemoteKey:   "vikasa/" + c.DOT + "/infrahub-token",
	}); err != nil {
		return nil, fmt.Errorf("k8s render: externalsecret %s: %w", c.ID, err)
	}

	var smBuf bytes.Buffer
	if err := serviceMonitorTmpl.Execute(&smBuf, serviceMonitorData{
		DOT: c.DOT, Cluster: c.ID, Namespace: c.Namespace, Release: c.PrometheusRelease,
	}); err != nil {
		return nil, fmt.Errorf("k8s render: servicemonitor %s: %w", c.ID, err)
	}
	var prBuf bytes.Buffer
	if err := prometheusRuleTmpl.Execute(&prBuf, prometheusRuleData{
		DOT: c.DOT, Cluster: c.ID, Namespace: c.Namespace, Release: c.PrometheusRelease, IsCentral: c.IsCentral,
	}); err != nil {
		return nil, fmt.Errorf("k8s render: prometheusrule %s: %w", c.ID, err)
	}

	var ksBuf bytes.Buffer
	if err := kustomizationTmpl.Execute(&ksBuf, prometheusRuleData{
		DOT: c.DOT, Cluster: c.ID, Namespace: c.Namespace, Release: c.PrometheusRelease, IsCentral: c.IsCentral,
	}); err != nil {
		return nil, fmt.Errorf("k8s render: kustomization %s: %w", c.ID, err)
	}

	out := map[string][]byte{
		"streams.yaml":        buf.Bytes(),
		"certificate.yaml":    certBuf.Bytes(),
		"externalsecret.yaml": esBuf.Bytes(),
		"kustomization.yaml":  ksBuf.Bytes(),
		"servicemonitor.yaml": smBuf.Bytes(),
		"prometheusrule.yaml": prBuf.Bytes(),
	}

	if c.IsCentral {
		var chBuf bytes.Buffer
		if err := credhealthRuleTmpl.Execute(&chBuf, credhealthRuleData{
			prometheusRuleData: prometheusRuleData{
				DOT: c.DOT, Cluster: c.ID, Namespace: c.Namespace, Release: c.PrometheusRelease,
			},
			MetricCredExpirySeconds:  credhealth.MetricCredExpirySeconds,
			MetricCredHealthLastScan: credhealth.MetricCredHealthLastScan,
		}); err != nil {
			return nil, fmt.Errorf("k8s render: credhealth-prometheusrule %s: %w", c.ID, err)
		}
		out["credhealth-prometheusrule.yaml"] = chBuf.Bytes()
	}

	return out, nil
}
