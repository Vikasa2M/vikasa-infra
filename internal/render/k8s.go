const banner = "# AUTOGENERATED by vikasa-infra/cmd/gen\n"

// streamCRTmpl is the text/template for a single NACK Stream CR.
// Conditional fields (maxAge, sources, filterSubject, subjectTransforms) are
// omitted when empty. A source with TransformSource emits a subjectTransforms
// block instead of filterSubject; the two are mutually exclusive.
var streamCRTmpl = template.Must(template.New("streamCR").Parse(`apiVersion: jetstream.nats.io/v1beta2
kind: Stream
metadata:
  name: {{ .MetaName }}
  labels:
    vikasa.dot: {{ .DOT }}
    vikasa.tier: {{ .Tier }}
  annotations:
    argocd.argoproj.io/sync-wave: "{{ .Wave }}"
spec:
  name: {{ .Name }}
  replicas: {{ .Replicas }}
  storage: file
  retention: limits
{{- if .MaxAge }}
  maxAge: {{ .MaxAge }}
{{- end }}
{{- if .MaxBytes }}
  maxBytes: {{ .MaxBytes }}
{{- end }}
{{- if .Duplicates }}
  duplicateWindow: {{ .Duplicates }}
{{- end }}
{{- if .RePublishSource }}
  republish:
    source: {{ .RePublishSource }}
    destination: {{ .RePublishDest }}
{{- end }}
{{- if .Sources }}
  sources:
{{- range .Sources }}
  - name: {{ .Name }}
    externalApiPrefix: $JS.{{ .Domain }}.API
{{- if .TransformSource }}
    subjectTransforms:
    - source: {{ .TransformSource }}
      dest: {{ .TransformDest }}
{{- else if .FilterSubject }}
    filterSubject: {{ .FilterSubject }}
{{- end }}
{{- end }}
{{- end }}
`))

// streamCRData is the template data for a single Stream CR.
type streamCRData struct {
	MetaName        string
	DOT             string
	Tier            string
	Wave            int
	Name            string
	Replicas        int
	Storage         string
	Retention       string
	MaxAge          string
	MaxBytes        int64
	Duplicates      string
	RePublishSource string
	RePublishDest   string
	Sources         []sourceData
}

type sourceData struct {
	Name            string
	Domain          string
	FilterSubject   string
	TransformSource string
	TransformDest   string
}

// toMetaName converts a NATS stream name (UPPER_SNAKE) to lower-kebab for k8s metadata.name.
func toMetaName(s string) string {
	return strings.ToLower(strings.ReplaceAll(s, "_", "-"))
}

// renderStreamCR renders a single Stream CR document (no leading "---").
func renderStreamCR(s plan.Stream, dot string) ([]byte, error) {
	var srcs []sourceData
	for _, src := range s.Sources {
		srcs = append(srcs, sourceData{
			Name:            src.Name,
			Domain:          src.Domain,
			FilterSubject:   src.FilterSubject,
			TransformSource: src.TransformSource,
			TransformDest:   src.TransformDest,
		})
	}

	data := streamCRData{
		MetaName:        toMetaName(s.Name),
		DOT:             dot,
		Tier:            string(s.Tier),
		Wave:            s.Tier.Wave(),
		Name:            s.Name,
		Replicas:        s.Replicas,
		MaxAge:          s.MaxAge,
		MaxBytes:        s.MaxBytes,
		Duplicates:      s.Duplicates,
		RePublishSource: s.RePublishSource,
		RePublishDest:   s.RePublishDest,
		Sources:         srcs,
	}

	var buf bytes.Buffer
	if err := streamCRTmpl.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("k8s render: stream %s: %w", s.Name, err)
	}
	return buf.Bytes(), nil
}

// certTmpl renders a cert-manager Certificate requesting the NATS server's TLS cert.
var certTmpl = template.Must(template.New("cert").Parse(`# AUTOGENERATED by vikasa-infra/cmd/gen
apiVersion: cert-manager.io/v1
kind: Certificate
metadata:
  name: {{ .Name }}
  labels:
    vikasa.dot: {{ .DOT }}
    vikasa.cluster: {{ .Cluster }}
spec:
  secretName: {{ .SecretName }}
  issuerRef:
    name: {{ .Issuer }}
    kind: ClusterIssuer
  duration: 2160h0m0s
  renewBefore: 1080h0m0s
  commonName: {{ .CommonName }}
  usages:
    - server auth
  dnsNames:
{{- range .DNSNames }}
    - {{ . }}
{{- end }}
`))

type certData struct {
	Name       string
	DOT        string
	Cluster    string
	SecretName string
	Issuer     string
	CommonName string
	DNSNames   []string
}

// externalSecretTmpl renders an External Secrets ExternalSecret that materializes
// the Infrahub inventory token Secret from an operator-provisioned
// ClusterSecretStore. It contains only a reference (store name + backend key
// path), never a secret value.
var externalSecretTmpl = template.Must(template.New("externalsecret").Parse(`# AUTOGENERATED by vikasa-infra/cmd/gen
apiVersion: external-secrets.io/v1beta1
kind: ExternalSecret
metadata:
  name: infrahub-token
  labels:
    vikasa.dot: {{ .DOT }}
    vikasa.cluster: {{ .Cluster }}
spec:
  refreshInterval: 1h
  secretStoreRef:
    name: {{ .SecretStore }}
    kind: ClusterSecretStore
  target:
    name: infrahub-token
    creationPolicy: Owner
  data:
    - secretKey: token
      remoteRef:
        key: {{ .RemoteKey }}
`))

type externalSecretData struct {
	DOT         string
	Cluster     string
	SecretStore string
	RemoteKey   string
}

