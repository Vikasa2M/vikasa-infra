// loadPlan loads a topology file and builds a Plan, failing the test on error.
func loadPlan(t *testing.T, path string) *plan.Plan {
	t.Helper()
	root, err := topology.Load(path)
	if err != nil {
		t.Fatalf("topology.Load(%q): %v", path, err)
	}
	p, err := plan.Build(root)
	if err != nil {
		t.Fatalf("plan.Build: %v", err)
	}
	return p
}

// goldenPath returns the path to a golden file for the given filename.
func goldenPath(filename string) string {
	return "testdata/golden/" + filename
}

// checkGolden compares got against the committed golden file.
// If the golden file does not exist, the test FAILS with a clear message
// unless the UPDATE_GOLDEN environment variable is set, in which case the
// golden file is written and the test passes (opt-in regeneration).
func checkGolden(t *testing.T, filename string, got []byte) {
	t.Helper()
	path := goldenPath(filename)
	existing, err := os.ReadFile(path)
	if errors.Is(err, os.ErrNotExist) {
		if os.Getenv("UPDATE_GOLDEN") != "" {
			if werr := os.WriteFile(path, got, 0o644); werr != nil {
				t.Fatalf("write golden %s: %v", path, werr)
			}
			t.Logf("wrote golden file %s (UPDATE_GOLDEN set)", path)
			return
		}
		t.Fatalf("golden file %s does not exist; run with UPDATE_GOLDEN=1 to create it", path)
	}
	if err != nil {
		t.Fatalf("read golden %s: %v", path, err)
	}
	if !bytes.Equal(existing, got) {
		if os.Getenv("UPDATE_GOLDEN") != "" {
			if werr := os.WriteFile(path, got, 0o644); werr != nil {
				t.Fatalf("write golden %s: %v", path, werr)
			}
			t.Logf("updated golden file %s (UPDATE_GOLDEN set)", path)
			return
		}
		t.Errorf("output differs from golden file %s\n--- golden ---\n%s\n--- got ---\n%s",
			path, string(existing), string(got))
	}
}

func TestK8sRenderer_RenderCluster(t *testing.T) {
	p := loadPlan(t, "../../examples/exdot-shared.json")

	var central []plan.Stream
	var regionalD7a []plan.Stream
	for _, s := range p.Streams {
		switch s.Cluster {
		case "core":
			central = append(central, s)
		case "d7a":
			regionalD7a = append(regionalD7a, s)
		}
	}

	centralFiles, err := render.K8sRenderer{}.RenderCluster(render.ClusterSlice{
		ID: "core", SubstrateType: "kubernetes", JSDomain: "core", DOT: p.DOT,
		IsCentral: true, Streams: central,
		LeafEndpoint: "leaf-core.nats.vikasa.exdot:7422", Namespace: "vikasa-core", IssuerName: "vikasa-ca",
		SecretStore: "vikasa-secrets",
	})
	if err != nil {
		t.Fatalf("RenderCluster(core): %v", err)
	}
	centralYAML, ok := centralFiles["streams.yaml"]
	if !ok {
		t.Fatal("core: streams.yaml not produced")
	}
	cs := string(centralYAML)
	if !strings.HasPrefix(cs, "# AUTOGENERATED") {
		t.Errorf("core streams.yaml: missing banner")
	}
	if !strings.Contains(cs, `sync-wave: "0"`) {
		t.Errorf("core streams.yaml: expected sync-wave \"0\"\n%s", cs)
	}
	// Central runs short retention (aggregation/routing, not the archive) and a
	// size bound — findings C1/C2.
	if !strings.Contains(cs, "maxAge: 15m") {
		t.Errorf("core streams.yaml: central must have short maxAge (15m)\n%s", cs)
	}
	if !strings.Contains(cs, "maxBytes:") {
		t.Errorf("core streams.yaml: central must be bounded (maxBytes)\n%s", cs)
	}
	if !strings.Contains(cs, "externalApiPrefix: $JS.d7a.API") || !strings.Contains(cs, "externalApiPrefix: $JS.d7b.API") {
		t.Errorf("core streams.yaml: expected both partition sources\n%s", cs)
	}

	coreCert := string(centralFiles["certificate.yaml"])
	for _, want := range []string{
		"kind: Certificate",
		"name: core-nats-server",
		"name: vikasa-ca",
		"server auth",
		"commonName: leaf-core.nats.vikasa.exdot",
		"- leaf-core.nats.vikasa.exdot",
		"- nats.vikasa-core.svc.cluster.local",
	} {
		if !strings.Contains(coreCert, want) {
			t.Errorf("core certificate.yaml missing %q\n%s", want, coreCert)
		}
	}

	d7aFiles, err := render.K8sRenderer{}.RenderCluster(render.ClusterSlice{
		ID: "d7a", SubstrateType: "kubernetes", JSDomain: "d7a", DOT: p.DOT,
		Streams:      regionalD7a,
		LeafEndpoint: "leaf-d7a.nats.vikasa.exdot:7422", Namespace: "vikasa-d7a", IssuerName: "vikasa-ca",
		SecretStore: "vikasa-secrets",
	})
	if err != nil {
		t.Fatalf("RenderCluster(d7a): %v", err)
	}
	d7aYAML := d7aFiles["streams.yaml"]
	ds := string(d7aYAML)
	if strings.Count(ds, "kind: Stream") != 1 {
		t.Errorf("d7a streams.yaml: expected exactly 1 Stream\n%s", ds)
	}
	if strings.Contains(ds, "sources:") {
		t.Errorf("d7a streams.yaml: regional shell must not have sources\n%s", ds)
	}
	if !strings.Contains(ds, `sync-wave: "1"`) {
		t.Errorf("d7a streams.yaml: expected sync-wave \"1\"\n%s", ds)
	}

	checkGolden(t, "clusters/core/streams.yaml", centralYAML)
	checkGolden(t, "clusters/d7a/streams.yaml", d7aYAML)
}

