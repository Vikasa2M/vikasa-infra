func TestK8sRenderer_StreamBounds(t *testing.T) {
	dmz := plan.Stream{
		Name: "VIKASA_EXDOT_DMZ", Cluster: "dmz", JSDomain: "dmz", Replicas: 3, Tier: "dmz",
		MaxAge: "1h", MaxBytes: 10 << 30, Duplicates: "5m",
	}
	reg := plan.Stream{
		Name: "VIKASA_EXDOT_D7_D7_0", Cluster: "dmz", JSDomain: "dmz", Replicas: 3, Tier: "regional",
		MaxAge: "6h", MaxBytes: 50 << 30,
	}
	files, err := render.K8sRenderer{}.RenderCluster(render.ClusterSlice{
		ID: "dmz", SubstrateType: "kubernetes", DOT: "exdot", JSDomain: "dmz",
		LeafEndpoint: "leaf-dmz.nats.vikasa.exdot:7422", IssuerName: "vikasa-ca", SecretStore: "vikasa-secrets",
		Streams: []plan.Stream{dmz, reg},
	})
	if err != nil {
		t.Fatalf("RenderCluster: %v", err)
	}
	s := string(files["streams.yaml"])
	for _, want := range []string{
		"maxBytes: 10737418240", // dmz 10 GiB
		"duplicateWindow: 5m",   // dmz dedup window
		"maxBytes: 53687091200", // regional 50 GiB
		"maxAge: 1h",
		"maxAge: 6h",
	} {
		if !strings.Contains(s, want) {
			t.Errorf("streams.yaml missing %q:\n%s", want, s)
		}
	}
	// Only the DMZ stream carries a dedup window.
	if strings.Count(s, "duplicateWindow:") != 1 {
		t.Errorf("expected exactly 1 duplicateWindow (dmz only):\n%s", s)
	}
}

