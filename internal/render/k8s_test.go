func TestK8sRenderer_DMZSourceTransform(t *testing.T) {
	// Build a DMZ stream with two sources that each carry a subject transform
	// (one per share, as required by the validated E1 config).
	dmzStream := plan.Stream{
		Name:     "VIKASA_EXDOT_DMZ",
		Cluster:  "dmz",
		JSDomain: "dmz",
		Replicas: 3,
		Tier:     "dmz",

		Sources: []plan.Source{
			{
				Name:            "VIKASA_EXDOT_CENTRAL",
				Domain:          "core",
				TransformSource: "vikasa.exdot.d1.>",
				TransformDest:   "vikasa.exdot.share.research.>",
			},
			{
				Name:            "VIKASA_EXDOT_CENTRAL",
				Domain:          "core",
				TransformSource: "vikasa.exdot.d1.hwy9.>",
				TransformDest:   "vikasa.peer.exdot.hwy9.>",
			},
		},
	}

	files, err := render.K8sRenderer{}.RenderCluster(render.ClusterSlice{
		ID:            "dmz",
		SubstrateType: "kubernetes",
		DOT:           "exdot",
		JSDomain:      "dmz",
		LeafEndpoint:  "leaf-dmz.nats.vikasa.exdot:7422",
		IssuerName:    "vikasa-ca",
		SecretStore:   "vikasa-secrets",
		Streams:       []plan.Stream{dmzStream},
	})
	if err != nil {
		t.Fatalf("RenderCluster(dmz): %v", err)
	}
	streamsYAML, ok := files["streams.yaml"]
	if !ok {
		t.Fatal("dmz: streams.yaml not produced")
	}
	s := string(streamsYAML)

	// Source name must be the real central stream, not a synthetic per-share name.
	if strings.Count(s, "name: VIKASA_EXDOT_CENTRAL") != 2 {
		t.Errorf("expected exactly 2 sources named VIKASA_EXDOT_CENTRAL:\n%s", s)
	}
	// Both externalApiPrefix entries must point to $JS.core.API.
	if strings.Count(s, "externalApiPrefix: $JS.core.API") != 2 {
		t.Errorf("expected exactly 2 externalApiPrefix: $JS.core.API:\n%s", s)
	}
	// subjectTransforms blocks must be present, not filterSubject.
	if !strings.Contains(s, "subjectTransforms:") {
		t.Errorf("expected subjectTransforms: block:\n%s", s)
	}
	if strings.Contains(s, "filterSubject:") {
		t.Errorf("DMZ sources must not emit filterSubject (transforms used instead):\n%s", s)
	}
	// Verify the two transform pairs.
	for _, want := range []string{
		"source: vikasa.exdot.d1.>",
		"dest: vikasa.exdot.share.research.>",
		"source: vikasa.exdot.d1.hwy9.>",
		"dest: vikasa.peer.exdot.hwy9.>",
	} {
		if !strings.Contains(s, want) {
			t.Errorf("streams.yaml missing %q:\n%s", want, s)
		}
	}
}

